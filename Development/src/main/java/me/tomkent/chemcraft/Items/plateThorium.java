import net.minecraft.item.Item;

public class plateThorium extends Item {

	private static String itemName = "Thorium plate";
	private static String itemTexture = "plateThorium";
	private static String itemNameSafe = "plateThorium";
	
        public plateThorium() {
                maxStackSize = 64;
                setCreativeTab(ChemCraft.tabChemcraftItems);
                setUnlocalizedName(ChemCraft.MODID + "_" + itemNameSafe);
                setTextureName(ChemCraft.MODID + ":" + itemTexture);
        }
}