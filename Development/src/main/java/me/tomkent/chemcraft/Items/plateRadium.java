import net.minecraft.item.Item;

public class plateRadium extends Item {

	private static String itemName = "Radium plate";
	private static String itemTexture = "plateRadium";
	private static String itemNameSafe = "plateRadium";
	
        public plateRadium() {
                maxStackSize = 64;
                setCreativeTab(ChemCraft.tabChemcraftItems);
                setUnlocalizedName(ChemCraft.MODID + "_" + itemNameSafe);
                setTextureName(ChemCraft.MODID + ":" + itemTexture);
        }
}