import net.minecraft.item.Item;

public class dustStrontium extends Item {

	private static String itemName = "Strontium dust";
	private static String itemTexture = "dustStrontium";
	private static String itemNameSafe = "dustStrontium";
	
        public dustStrontium() {
                maxStackSize = 64;
                setCreativeTab(ChemCraft.tabChemcraftItems);
                setUnlocalizedName(ChemCraft.MODID + "_" + itemNameSafe);
                setTextureName(ChemCraft.MODID + ":" + itemTexture);
        }
}